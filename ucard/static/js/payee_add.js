        $('#legalTypeInput').on('click', function() {
            toggleDropdown($(this).closest('.custom-select-container'));
        });
        
        // 法人类型选项点击
        $('#legalTypeOptions').on('click', '.option-item', function() {
            var value = $(this).data('value');
            var text = $(this).text();
            $('#legalTypeInput').val(text);
            $('#legalTypeValue').val(value);
            toggleDropdown($(this).closest('.custom-select-container'));
        });
        
