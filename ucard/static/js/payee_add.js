    // 根据flattenData找到可用的收款方式
    function findAvailablePaymentMethods(flattenData) {
        // 获取当前国家和账户类型
        var country = flattenData.country;
        var accountType = flattenData.account_type;
        
        console.log("筛选收款方式 - 国家:", country, "账户类型:", accountType); // 添加日志
        console.log("可用的收款方式列表:", paymentMethods); // 添加日志
        
        // 根据账户类型筛选不同的支付方式
        var methods = paymentMethods.filter(function(method) {
            // 如果是电子钱包账户，排除BANK_TRANSFER
            if (accountType === 'E_WALLET') {
                var isMatch = (method.code !== 'BANK_TRANSFER') && 
                            (method.country === country || !method.country || 
                            (!method.country && method.currency));
                
                console.log("检查方式:", method.code, "- 国家:", method.country, 
                          "- 币种:", method.currency, "- 匹配:", isMatch, 
                          "- 原因: E_WALLET账户，排除BANK_TRANSFER"); 
                
                return isMatch;
            } 
            // 如果是银行账户，只显示BANK_TRANSFER
            else if (accountType === 'BANK_ACCOUNT') {
                var isMatch = method.code === 'BANK_TRANSFER';
                
                console.log("检查方式:", method.code, "- 国家:", method.country, 
                          "- 币种:", method.currency, "- 匹配:", isMatch, 
                          "- 原因: BANK_ACCOUNT账户，只显示BANK_TRANSFER");
                
                return isMatch;
            }
            // 其他情况保持原有筛选逻辑
            else {
                var isMatch = (
                    (method.country === country || !method.country) ||
                    (!method.country && method.currency)
                );
                
                console.log("检查方式:", method.code, "- 国家:", method.country, 
                          "- 币种:", method.currency, "- 匹配:", isMatch, 
                          "- 原因: 其他账户类型，使用默认规则");
                
                return isMatch;
            }
        });
        
        console.log("筛选后的收款方式:", methods); // 添加日志
        
        return methods;
    }
    
