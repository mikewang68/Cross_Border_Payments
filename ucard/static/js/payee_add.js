    // 切换下拉框显示/隐藏
    function toggleDropdown(container) {
        var options = container.find('.custom-select-options');
        
        // 先关闭所有其他下拉框
        $('.custom-select-options').not(options).hide();
        $('.custom-select-container').not(container).removeClass('custom-select-active');
        
        // 切换当前下拉框
        container.toggleClass('custom-select-active');
        options.toggle();
    }
    
    // 根据选择的国家更新币种选项
    function updateCurrencyOptions(countryCode) {
        var currencyOptions = $('#currencyOptions');
        currencyOptions.empty();
        
        // 清空当前币种选择
        $('#currencyInput').val('');
        $('#currencyValue').val('');
        
        // 根据国家和账户类型筛选可用币种
        var accountType = $('input[name="account_type"]:checked').val();
        var availableCurrencies = [];
        
        regionsData.forEach(function(item) {
            if(item.country === countryCode && item.payment_method === 'BANK_TRANSFER') {
                availableCurrencies = item.currencies;
                return false; // 找到后退出循环
            }
        });
        
        // 填充币种选项
        if(availableCurrencies && availableCurrencies.length > 0) {
            availableCurrencies.forEach(function(currency) {
                currencyOptions.append(`<div class="option-item" data-value="${currency}">${currency}</div>`);
            });
            
            // 币种选项点击
            $('#currencyOptions').off('click', '.option-item').on('click', '.option-item', function() {
                var value = $(this).data('value');
                $('#currencyInput').val(value);
                $('#currencyValue').val(value);
                toggleDropdown($(this).closest('.custom-select-container'));
            });
        } else {
            currencyOptions.append('<div class="no-options">该国家/地区无可用币种</div>');
        }
    }
    
